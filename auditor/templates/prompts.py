def build_audit_prompt(product_text: str, platform: str, url: str) -> str:
    safe_text = product_text.replace("{", "{{").replace("}", "}}")
    return AUDIT_PROMPT_TEMPLATE.format(
        platform=platform,
        url=url,
        product_text=safe_text[:5000],
    )
